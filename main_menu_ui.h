inline void drawMenuCell(int idx, bool selected) {
  int row = idx / 3, col = idx % 3;
  int cx = CELL_X[col];
  int cy = CELL_Y[row];
  int iw = MENU_ICONS[idx].w;   //   fixed: was "1W"
  int ih = MENU_ICONS[idx].h;
  int ix = cx + (CELL_W - iw) / 2;
  int iy = cy + (CELL_H - ih) / 2;

  if (selected) {
    tft.fillRect(cx, cy, CELL_W, CELL_H, C_TXT);   //   fixed: was C_INT
    tft.drawRect(cx - 1, cy - 1, CELL_W + 2, CELL_H + 2, C_SEL);
    tft.drawBitmap(ix, iy, MENU_ICONS[idx].bmp, iw, ih, C_BG);
  } else {
    tft.fillRect(cx, cy, CELL_W, CELL_H, C_BG);
    tft.drawRect(cx, cy, CELL_W, CELL_H, C_TXT);
    tft.drawBitmap(ix, iy, MENU_ICONS[idx].bmp, iw, ih, C_TXT);
  }
}

inline void drawMenuLabel(int idx) {
  tft.fillRect(0, 114, 128, 14, C_HDR);
  tft.setTextColor(C_SEL);
  tft.setTextSize(1);
  const char* lbl = MENU_LABELS[idx];
  int lx = (128 - (int)strlen(lbl) * 6) / 2;
  tft.setCursor(lx, 118);
  tft.print(lbl);
}

inline void drawMainMenu() {
  tft.fillScreen(C_BG);
  tft.drawLine(0, 13, 128, 13, C_TXT);
  for (int i = 0; i < 9; i++) drawMenuCell(i, i == 0);
  drawMenuLabel(0);
}

